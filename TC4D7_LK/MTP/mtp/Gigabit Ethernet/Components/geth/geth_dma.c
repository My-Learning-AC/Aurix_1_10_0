    l_readDscr_p->dscr1.reserved = 0; /* set all descriptor parts to 0 */
    l_readDscr_p->dscr2.BUF2AP   = 0; /* set all descriptor parts to 0 */
    l_readDscr_p->dscr3.U        = 0; /* set all descriptor parts to 0 */

