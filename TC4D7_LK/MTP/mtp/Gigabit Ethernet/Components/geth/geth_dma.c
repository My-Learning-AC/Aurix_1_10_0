        l_ctxtDscr_p->dscr1.RTSH = 0;
        l_ctxtDscr_p->dscr2.reserved = 0;
        l_ctxtDscr_p->dscr3.U = 0;
        l_ctxtDscr_p->dscr3.B.OWN = 1;
