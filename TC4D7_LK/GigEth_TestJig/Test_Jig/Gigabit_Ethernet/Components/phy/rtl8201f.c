/* RTL8201F PHY Status Register */
#define RTL8201F_PHY_STATUS          0x1
#define RTL8201F_PHY_STATUS_SPEED    0x2000
#define RTL8201F_PHY_STATUS_100      0x2000
#define RTL8201F_PHY_STATUS_10       0x0000
#define RTL8201F_PHY_STATUS_DUPLEX   0x0100
#define RTL8201F_PHY_STATUS_LINK     0x0004

#define RTL8201F_PAGE_SELECT         0x1f

#define RTL8201F_TX_DELAY_EN         0x0100
#define RTL8201F_RX_DELAY_EN         0x0004

#define RTL8201F_PHY_LEDCTRL_LED0_LINK10   0x0001
#define RTL8201F_PHY_LEDCTRL_LED0_LINK100  0x0002
#define RTL8201F_PHY_LEDCTRL_LED0_ACT      0x0008
#define RTL8201F_PHY_LEDCTRL_LED1_LINK10   0x0010
#define RTL8201F_PHY_LEDCTRL_LED1_LINK100  0x0020
#define RTL8201F_PHY_LEDCTRL_LED1_ACT      0x0080
#define RTL8201F_PHY_LEDCTRL_LED2_LINK10   0x0100
#define RTL8201F_PHY_LEDCTRL_LED2_LINK100  0x0200
#define RTL8201F_PHY_LEDCTRL_LED2_ACT      0x0800

#define RTL8201F_PHY_ID                    0x001CC810      /* PHY_ID for RTL8201f */
#define RTL8201F_PHY_ID_MSK                0xFFFFFFF0      /* PHY_ID for RTL8201f */

