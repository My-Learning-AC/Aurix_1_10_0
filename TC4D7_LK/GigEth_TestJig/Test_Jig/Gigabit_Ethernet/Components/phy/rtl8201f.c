sint32 RTL8201F_ConfigureLeds(ETH_PHY_t *const handle)
{
    uint16 regval;
    uint8 phy_addr = handle->phyAddr_u8;

    handle->ethPhyFunc.mdio_write(phy_addr, RTL8201F_PAGE_SELECT, 0x7);
    handle->ethPhyFunc.mdio_read(phy_addr, 19, &regval);
    regval |= (1 << 3); /* 0 = customized led disabled, 1 = customized led enabled */
    handle->ethPhyFunc.mdio_write(phy_addr, 19, regval);

    handle->ethPhyFunc.mdio_write(phy_addr, 0x11, RTL8201F_PHY_LEDCTRL_LED0_LINK10 | RTL8201F_PHY_LEDCTRL_LED0_ACT |
                                  RTL8201F_PHY_LEDCTRL_LED1_LINK100 | RTL8201F_PHY_LEDCTRL_LED1_ACT);
    /* restore to default page 0 */
    handle->ethPhyFunc.mdio_write(phy_addr, RTL8201F_PAGE_SELECT, 0x0);

    return 0;
}

