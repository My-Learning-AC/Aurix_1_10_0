sint32 Rtl8201F_Init(ETH_PHY_t *const phyInstance_p)
{
    sint32 result_s32;
    result_s32 = FAILURE;

    if (phyInstance_p == NULL)
    {
        return FAILURE;
    }

    /*********************************************************************************************************************************************/
    /* Realtek: init Phy                                                                                                                         */
    /*********************************************************************************************************************************************/
    if (RTL8201F_Phy_Init(phyInstance_p, (uint32)(ETH_PHY_CONFIG_INTERFACE_RMII | ETH_PHY_CONFIG_AUTONEG | ETH_PHY_CONFIG_ENABLE_CLKOUT)) == SUCCESS)
    {
        printf("Phy%i: RTL8201F_Init() = SUCCESS"EOL, phyInstance_p->hsphyPort_u8);
    }
    else
    {
        printf("Phy%i: RTL8201F_Init() = FAIL"EOL, phyInstance_p->hsphyPort_u8);
        return FAILURE;
    }

    return SUCCESS;
}

sint32 Rtl8201f_EthLinkUp(ETH_PHY_t *const phyInstance_p, uint16 * const speed_p)
{
    sint32 result_s32;
    uint32 timeout_u32;
    boolean linkActive_b;

    result_s32 = FAILURE;

    if (phyInstance_p == NULL)
    {
        return FAILURE;
    }

    /*********************************************************************************************************************************************/
    /* Realtek: wait for AutoNegotiation                                                                                                        */
    /*********************************************************************************************************************************************/
    if (RTL8201F_IsAutonegotiationEnabled(phyInstance_p))
    {
        while (RTL8201F_IsAutonegotiationDone(phyInstance_p) == FALSE);
    }

    /*********************************************************************************************************************************************/
    /* Realtek: check ethernet link state                                                                                                        */
    /*********************************************************************************************************************************************/

    printf("Phy%i: wait for Rtl8201F_is_link()"EOL, phyInstance_p->macPort_u8);
    timeout_u32 = 0xFFFF;
    do
    {
        result_s32 = RTL8201F_GetLinkStatus(phyInstance_p);
        timeout_u32--;
    }
    while ((result_s32 != ETH_PHY_LINK_STATUS_UP) && (timeout_u32 > 0));

    if ((timeout_u32 == 0) || (result_s32 != ETH_PHY_LINK_STATUS_UP))
    {
        return FAILURE;
    }

    linkActive_b = TRUE;
    /*********************************************************************************************************************************************/
    /* Realtek: get ethernet link speed                                                                                                          */
    /*********************************************************************************************************************************************/
    *speed_p = (uint16)RTL8201F_GetLinkSpeed(phyInstance_p);


    if (linkActive_b == TRUE)
    {
        printf("Phy%i: Eth Link up, Speed = %i"EOL, phyInstance_p->macPort_u8, (int)*speed_p);
    }
    else
    {
        printf("Phy%i: Eth Link down, Speed = %i"EOL, phyInstance_p->macPort_u8, (int)*speed_p);
        return FAILURE;
    }

    return SUCCESS;
}
